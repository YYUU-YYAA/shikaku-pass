  id: ExamId;
  name: string;        // 表示名 例: "CMA（証券アナリスト）"
  shortName: string;   // 例: "CMA Pass"
  implemented: boolean; // 問題データが実装済みか（false="準備中"バッジ表示用）
  /** 学習時間の目安。例: "200〜350時間（3級知識前提なら200〜300時間）" */
