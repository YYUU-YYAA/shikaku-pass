/**
 * 職種カテゴリのキー。
 * career-roadmaps.md の初期5職種＋前回会議で列挙したその他職種。
 */
export type RoleId =
  | 'management'    // 経営・経営企画
  | 'accounting'    // 経理・財務
  | 'legal'         // 法務・コンプライアンス
  | 'hr'            // 人事・労務
  | 'rd'            // 研究開発・技術
  | 'production'    // 生産管理・品質管理
  | 'is'            // 情報システム・IT
  | 'sales'         // 営業・マーケティング
  | 'logistics'     // 物流・購買
  | 'finance';      // 金融・investment

/**
 * ロードマップ1ステップ = 1資格 + 編集的コメント。
 * order はフェーズ内での推奨順（1始まり）。
 * 'parallel' フェーズ内では「この分岐内ではこの順で取るとよい」という
 * 意味になる（分岐そのものの優先順位ではない）。
 */
export interface RoadmapStep {
  examId: ExamId;
  order: number;
  note: string; // 「なぜこの資格・順番か」の一言（資格Passとしてのポジション）
}

/**
 * ロードマップ1フェーズ = 複数ステップ + フェーズ全体の編集コメント。
 *
 * - type: 'sequence' → ステップを順番に進む直列フェーズ（例: コア区間）。
 *         画面上は矢印付きの一直線リストで表示する。
 * - type: 'parallel' → 複数の分岐を並列に提示するフェーズ。
 *         画面上は横並びカード（または縦積みのタブ）で表示する想定。
 *         「並列」は分岐そのものを指す単位として、分岐1つ = フェーズ1つ
 *         とする（例: 経理職フェーズ2-A/2-B/2-Cはそれぞれ1つのPhase）。
 */
export interface RoadmapPhase {
  id: string;               // フェーズの識別子 例: 'core', 'branch-a'
  title: string;            // フェーズの見出し 例: "フェーズ1: コア（ここから始める）"
  type: 'sequence' | 'parallel';
  description: string;      // フェーズ全体の編集コメント（「なぜこの構成にしたか」）
  steps: RoadmapStep[];
}

export interface RoleDefinition {
  id: RoleId;
  name: string;          // 表示名 例: "経理・財務"
  description: string;   // 職種の概要・どんな人向けか
  /** この職種のロードマップ（フェーズの配列）。表示順 = 配列順。 */
  roadmap: RoadmapPhase[];
  /** 職種ハブ画面でのアイコン（emoji。実装時は専用アイコンに置換可） */
  icon: string;
}

