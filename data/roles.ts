/** 指定した資格が、どの職種ロードマップに含まれているかを逆引き */
export function getRolesByExam(examId: ExamId): RoleDefinition[] {
  return ROLES.filter((role) =>
    role.roadmap.some((phase) => phase.steps.some((step) => step.examId === examId))
  );
}

/** 職種IDからロードマップ（フェーズの配列）を取得 */
export function getRoadmap(roleId: RoleId): RoadmapPhase[] {
  const role = ROLES.find((r) => r.id === roleId);
  if (!role) return [];
  return role.roadmap;
}

/** 職種IDから、ロードマップ内の全ステップをフェーズ順・order順で取得（フラット表示用） */
export function getRoadmapSteps(roleId: RoleId): RoadmapStep[] {
  const role = ROLES.find((r) => r.id === roleId);
  if (!role) return [];
  return role.roadmap.flatMap((phase) => [...phase.steps].sort((a, b) => a.order - b.order));
}

/** 指定した資格の定義を取得 */
export function getExamDefinition(examId: ExamId): ExamDefinition | undefined {
  return EXAMS.find((exam) => exam.id === examId);
}
